export const add = function (a : number, b : number) : number {
    return a + b;
}

export const sub = function (a : number, b : number) : number {
    return a - b;
}

export const mul = function (a : number, b : number) : number {
    return a * b;
}

export const div = function (a : number, b : number) : number {
    return a / b;
}

